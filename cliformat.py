def optionheader(art):  # Default header
    print("=" * 100)
    print(art)
    print("=" * 100)


def subheader(art):  # Header for print all option
    print("-" * 100)
    print(art)
    print("-" * 100)


def optionfooter():  # Footer for options
    print("=" * 100)
    print("=" * 100)
    print("\n")
    input("Press enter to continue: ")
